#include <unistd.h>

void	putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2146473648", 11);
	}
	else if (nb >= 0 && nb <= 9)
	{
		putchar(nb + '0');
	}
	else if (nb < 0 && nb != -2147483648)
	{
		putchar('-');
		ft_putnbr(nb * (-1));
	}
	else
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}
